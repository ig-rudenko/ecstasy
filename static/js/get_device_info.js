String.prototype.format = function () {
    let args = arguments;

    return this.replace(/{([0-9]+)}/g, function (match, index) {
    return typeof args[index] == 'undefined' ? match : args[index];
  });
};

let utilization_info = document.getElementById("utilization-info")

function get_device_info() {
    let request_url = window.location.pathname

    $.ajax({
        url: request_url + "/info",
        type: 'GET',
        success: function( data ) {

            if (data) {
                utilization_info.innerHTML = "<h4>Загруженность</h4>"
            }
            console.log(data)
            for (let key of Object.keys(data)) {
                console.log(key, data[key])
                if (data[key] && data[key].util){
                    let color
                    if (data[key].util < 30) {
                        color = "#198754"
                    } else if (data[key].util < 80) {
                        color = "#ff9836"
                    } else {
                        color = "#dc3545"
                    }
                    utilization_info.innerHTML += `<div class="btn">`+
                        INFO_ICONS[key].format(color) + ` ${key} ${data[key].util}%</div>`
                }
            }
        },
    });

}


